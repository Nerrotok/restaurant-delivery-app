
public class Customer {

	// Customer class attributes
	String name;
	String contactNumber;
	String streetAddress;
	String suburb;
	String city;
	String emailAddress;
	
	// Customer class constructor
	public Customer(String name,String contactNumber, String streetAddress, String suburb ,String city, String emailAddress) {
		this.name = name;
		this.contactNumber = contactNumber;
		this.streetAddress = streetAddress;
		this.suburb = suburb;
		this.city = city;
		this.emailAddress = emailAddress;
		
	}
}

